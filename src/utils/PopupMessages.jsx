import { toast } from 'react-toastify';
import 'react-toastify/dist/ReactToastify.css';

export const errorPopup = (message) => {
    toast.error(message, {
        position: "top-right",
        autoClose: 2000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
    })
}

